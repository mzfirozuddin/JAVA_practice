public class Add2Num {
    public static void main(String[] args) {
        int a=24;
        int b=40;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(b/a);
        System.out.println(b*a);
        float c=24.5F;
        float d=40.3F;
        System.out.println(c+d);
        double e=24.5002;
        double f=405.88;
        System.out.println(e+f);
    }
}
