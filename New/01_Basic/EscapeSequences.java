class EscapeSequences
{
    public static void main(String[] args) {
       System.out.println("Welcome to \"GFG\""); 
       System.out.println("Hi, \nWelcome to course"); 
       System.out.println("C:\\xyz\\abc.txt"); 
    }
}

//Note:-
// The Valid Escape Sequences Are:
// \t, \b, \n, \r, \f, \', \", \\