public class WrapperClass {
    public static void main(String[] args) {
        int x1 = 10; // Primitive Type
        Integer x2 = new Integer(10);  // Wrapper Class
        System.out.println(x1);
        System.out.println(x2);
    }
}


// Note:- 
//  Primitive Type    Wrapper Class
//      char       =>  Character
//      byte       =>  Byte
//      short      =>  Short
//      int        =>  Integer
//      long       =>  Long
//      float      =>  Float
//      double     =>  Double
//      boolean    =>  Boolean