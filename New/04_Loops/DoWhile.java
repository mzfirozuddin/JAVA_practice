public class DoWhile {
    public static void main(String[] args) {
        int i = 0;
        do {
           System.out.println("firoz"); 
           i++;
        } while (i<10);
    }
}


// Syntax:-

/* 
do {
    
   // Statement to be execute    

} while (condition); 
*/


// for and while loop are called entry control loop because condition check in entry time
// do-while loop is called exit control loop because condition check in exit time

